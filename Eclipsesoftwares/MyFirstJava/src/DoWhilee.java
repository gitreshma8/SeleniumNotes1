
public class DoWhilee {

	public static void main(String[] args) {
		
		int a=0;
		
		while(a<10) {
			
			if(a%2==0) {
				
				System.out.println(a +" "+"an even number");
				System.out.println();
				}
		
			
			else if(a%2==1) {
				
				System.out.println(a +" "+"an odd number");
				System.out.println();
			}
			
			a++;
		}
		
	}
	
}

		
