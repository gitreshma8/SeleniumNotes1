
public class OddEvenBreak {

	public static void main(String[] args) {
	
		
		System.out.println("for even Condition");
		for (int i=0;i<10;) {	
			
			i++;
					
			System.out.println(i);
			if(i%2==0)
			
				break;
			

			
			
		}
		
		System.out.println("for odd Condition");
		for (int i=0;i<10;) {	
			
			i++;	
			System.out.println(i);
			
			if(i%1==0)
				break;
			
		}
		
		
	}

}
